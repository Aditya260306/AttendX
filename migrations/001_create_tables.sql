-- ─── 6. HOLIDAYS ───────────────────────────────────────────────
CREATE TABLE public.holidays (
  id            SERIAL PRIMARY KEY,
  holiday_date  DATE         NOT NULL UNIQUE,
  name          VARCHAR(200) NOT NULL,
  type          VARCHAR(20)  DEFAULT 'public'  -- public | optional
);

-- ─── 7. DEVICE COMMANDS (Dashboard → Agent → Machine) ──────────
CREATE TABLE public.device_commands (
  id            BIGSERIAL PRIMARY KEY,
  device_id     BIGINT       NOT NULL REFERENCES public.devices(id) ON DELETE CASCADE,
  command_type  VARCHAR(50)  NOT NULL,
  -- command_type values:
  --   sync_attendance, sync_users, add_user, delete_user,
  --   sync_time, restart, clear_logs, get_info
  payload       JSONB        DEFAULT '{}',
  status        VARCHAR(20)  DEFAULT 'pending',  -- pending | processing | completed | failed
  result        TEXT,
  created_by    TEXT,
  created_at    TIMESTAMPTZ  DEFAULT NOW(),
  executed_at   TIMESTAMPTZ
);

CREATE INDEX idx_commands_pending ON public.device_commands (status) WHERE status = 'pending';

-- ─── 8. AGENT HEARTBEAT ────────────────────────────────────────
CREATE TABLE public.agent_heartbeat (
  id            SERIAL PRIMARY KEY,
  agent_id      VARCHAR(100) NOT NULL UNIQUE,
  last_ping     TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
  ip_address    VARCHAR(45),
  version       VARCHAR(20)  DEFAULT '1.0.0',
  status        VARCHAR(20)  DEFAULT 'online',
  machine_statuses JSONB     DEFAULT '{}',
  updated_at    TIMESTAMPTZ  DEFAULT NOW()
);

-- ─── 9. SYNC HISTORY ───────────────────────────────────────────
CREATE TABLE public.sync_history (
  id              BIGSERIAL PRIMARY KEY,
  device_id       BIGINT,
  device_name     VARCHAR(100),
  action          VARCHAR(50)  NOT NULL,
  records_count   INTEGER      DEFAULT 0,
  status          VARCHAR(20)  DEFAULT 'success',  -- success | error
  message         TEXT,
  created_at      TIMESTAMPTZ  DEFAULT NOW()
);

-- ─── 10. AUDIT LOG ─────────────────────────────────────────────
CREATE TABLE public.audit_log (
  id              BIGSERIAL PRIMARY KEY,
  user_email      TEXT,
  action          VARCHAR(100) NOT NULL,
  entity_type     VARCHAR(50),
  entity_id       TEXT,
  old_value       JSONB,
  new_value       JSONB,
  ip_address      VARCHAR(45),
  created_at      TIMESTAMPTZ  DEFAULT NOW()
);

CREATE INDEX idx_audit_log_time ON public.audit_log (created_at DESC);

